<h1>Greetings!</h1>
<br>
<p>
As you might have heard, MAG Lab had its annual meeting of the membership and board on November 28th to discuss the future and sustainability of MAG Lab.  MAG Lab has been in a bit of a valley for the past year in regards to membership and funds, for a number of reasons, and we needed to address these and other issues if MAG Lab was to continue to stay open.
</p>
<p>We determined that in order to just pay for rent and utilities, we'll need to bump up membership fees and membership count simultaneously and maintain it over the long run.  However, we are still in a bind in regards to the short term, namely until the end of February where we will re-convene and re-assess the situation here at MAG Lab to make the next move .  This is where we look to you, the people who already understand the value of MAG Lab for yourselves and the community abroad, to help take up the slack while we implement new approaches to increasing the amount and types of income for MAG Lab and its sustainability.</p>
<p>
Membership Dues will be increased effective January 1st 2016.
<strong style='font-size:bold;'>
The new rates will be $40 for General Membership, and $90 for keyed membership.
</strong>
While this may be hard on some, these rates are well below what it would take to support the rent and utilities for MAG Lab with our current amount of membership.  As such, we ask anyone who is able to donate above and beyond membership dues to do so by making a pledge.  All pledges will go towards making up the disparity between income (membership dues) and expenses (rent, electricity, internet, gas, toiletries).
</p>
<p>MAG Lab is an awesome group of people whom have come together to do extraordinary things and an excellent space that has grown and improved as a resource to the community and reflects the participation and support which is put into it.  It is this participation and support which inspires others to support and participate in the MAG Lab community, a feedback loop of sorts.</p>
<p>Consider pledging a little extra, upgrading membership, or just plain donating to not only ensure that MAG Lab is here in the future, but that it and the community around it are thriving.</p>
<br>
<p>
Kind Regards,
<br>
Trent Wilson
<br>
President
<br>
MAG Lab
